package Net.server.maps;

public enum FieldLimitType {

    MOVELIMIT(0x1),
    SKILLLIMIT(0x2),
    SUMMONLIMIT(0x04),
    MYSTICDOORLIMIT(0x08),
    MIGRATELIMIT(0x10),
    PORTALSCROLLLIMIT(0x20),
    TELEPORTITEMLIMIT(0x40),
    MINIGAMELIMIT(0x80),
    SPECIFICPORTALSCROLLLIMIT(0x100),
    TAMINGMOBLIMIT(0x200),
    STATCHANGEITEMCONSUMELIMIT(0x400),
    PARTYBOSSCHANGELIMIT(0x800),
    NOMOBCAPACITYLIMIT(0x1000),
    WEDDINGINVITATIONLIMIT(0x2000),
    CASHWEATHERCONSUMELIMIT(0x4000),
    NOPET(0x8000),
    ANTIMACROLIMIT(0x10000),
    FALLDOWNLIMIT(0x20000),
    SUMMONNPCLIMIT(0x40000),
    NOEXPDECREASE(0x80000),
    NODAMAGEONFALLING(0x100000),
    PARCELOPENLIMIT(0x200000),
    DROPLIMIT(0x400000),
    ROCKETBOOSTER_LIMIT(0x800000),
    ITEMOPTIONLIMIT(0x1000000),
    NOQUESTALERTLIMIT(0x2000000),
    NOANDROID(0x4000000),
    AUTO_EXPAND_MINIMAP(0x8000000),
    MOVESKILLONLY(0x10000000),
    ONLYSTARPLANETPET(0x20000000);


    private final int i;

    FieldLimitType(int i) {
        this.i = i;
    }

    public int getValue() {
        return i;
    }

    public boolean check(int fieldlimit) {
        return (fieldlimit & i) == i;
    }
}
